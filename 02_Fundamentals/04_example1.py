# Convert string to integer
num_str = "10"
num_int = int(num_str)
print(num_int) #output: 10
print(type(num_int))

# Convert integer to string
num_int = 43
num_str = str(num_int)
print(num_str) # output: "43"
print(type(num_str))

# Convert float to integer
num_float = 3.14
num_int = int(num_float)
print(num_int) #output: 3
print(type(num_int)) 