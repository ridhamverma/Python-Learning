name = "Ridham"
print(name[0:2]) # goes from 0 to 2-1 i.e; 0 to 1

print(name[2:-1]) # same as name[2:4]

# print(name[0:6:n]) # skip n - 1 characters
print(name[0:6:1]) # skip 0 character
print(name[0:6:2]) # skip 1 character
print(name[0:6:3]) # skip 2 character
print(name[0:6:4]) # skip 3 character

print(name[:4]) # Replace the first empty number with 0 # name[0:4]
print(name[1:]) # Replace the second empty number with length -1 # name[1:6]